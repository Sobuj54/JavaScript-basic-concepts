// normal ternary operator
var val = 11;

val === 10 ? console.log("ten") : console.log("not ten");

// ------------------->>--------------------->>>
// nesting ternary operator
var num = 50;

num > 50
  ? console.log("more than 50")
  : num < 50
  ? console.log("num is less than 50")
  : console.log("num is 50");
